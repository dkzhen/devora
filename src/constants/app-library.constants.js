// Utility Functions
export const formatFileSize = (bytes) => {
  if (!bytes) return 'Unknown';
  const size = parseInt(bytes);
  if (size < 1024) return size + ' B';
  if (size < 1024 * 1024) return (size / 1024).toFixed(2) + ' KB';
  if (size < 1024 * 1024 * 1024) return (size / (1024 * 1024)).toFixed(2) + ' MB';
  return (size / (1024 * 1024 * 1024)).toFixed(2) + ' GB';
};

export const capitalizeWords = (str) => {
  if (!str) return '';
  return str.split(' ').map(word => 
    word.charAt(0).toUpperCase() + word.slice(1).toLowerCase()
  ).join(' ');
};

export const cleanFilename = (appName, version) => {
  const cleanAppName = appName.replace(/\s+/g, '').replace(/[^a-zA-Z0-9]/g, '');
  const rawVersion = version.replace(/\s+/g, '').replace(/[^a-zA-Z0-9\.]/g, '');
  const cleanVersion = rawVersion.toLowerCase().startsWith('v') ? rawVersion : `v${rawVersion}`;
  return `Devora-${cleanAppName}-${cleanVersion}.apk`;
};

